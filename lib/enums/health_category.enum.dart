enum HealthCategory {
  movement,    // Physical movement like steps, distance, flights climbed
  energy,      // Energy/calories related like expended, dietary, net calories
  exercise,    // Exercise and workout related activities
  nutrition,   // Food, vitamins, minerals, and dietary intake
  body,        // Body measurements like weight, height, body fat
  health,      // Health metrics like heart rate, blood pressure
  wellness,    // Wellness related metrics
} 
